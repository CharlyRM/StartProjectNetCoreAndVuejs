<script>
import { validUsername, validEmail } from '@/utils/validate'
import Swal from 'sweetalert2'
import store from '@/store'
import { create } from '@/api/user'

export default {
  name: 'Login',
  data() {
    const validateEmail = (rule, value, callback) => {
      if (!validEmail(value)) {
        callback(new Error('Enter the correct user email'))
      } else {
        callback()
      }
    } 
    const validatePassword = (rule, value, callback) => {
      if (value.length < 6) {
        callback(new Error('Password must be at least 6 digits!'))
      } else {
        callback()
      }
    }
    return {
      loginForm: {
        username: 'ribeiroc97@hotmail.com',
        password: '123456'
      },
      loginRules: {
        username: [{ required: true, trigger: 'blur', validator: validateEmail }],
        password: [{ required: true, trigger: 'blur', validator: validatePassword }]
      },
      passwordType: 'password',
      capsTooltip: false,
      loading: false,
      loadingCadastro: false,
      showDialog: false,
      redirect: undefined,
      otherQuery: {},

      user: {
        idUser: 0,
        idCompany: 0,
        name: '',
        email: '',
        password: '',
        status: 1,
        firstAccess: '',
        administrator: ''
      }      
    }
  },
  watch: {
    $route: {
      handler: function(route) {
        const query = route.query
        if (query) {
          this.redirect = query.redirect
          this.otherQuery = this.getOtherQuery(query)
        }
      },
      immediate: true
    }
  },  
  mounted() {
    if (this.loginForm.username === '') {
      this.$refs.username.focus()
    } else if (this.loginForm.password === '') {
      this.$refs.password.focus()
    }
  },
  methods: { 
    checkCapslock({ shiftKey, key } = {}) {
      if (key && key.length === 1) {
        if (shiftKey && (key >= 'a' && key <= 'z') || !shiftKey && (key >= 'A' && key <= 'Z')) {
          this.capsTooltip = true
        } else {
          this.capsTooltip = false
        }
      }
      if (key === 'CapsLock' && this.capsTooltip === true) {
        this.capsTooltip = false
      }
    },
    directPageHome(){
      window.location.href = 'https://localhost:44345/Home';
    },
    showPwd() {
      if (this.passwordType === 'password') {
        this.passwordType = ''
      } else {
        this.passwordType = 'password'
      }
      this.$nextTick(() => {
        this.$refs.password.focus()
      })
    },  
    runLogin() {
      this.user.email = this.loginForm.username;
      this.user.password = this.loginForm.password;  

      this.$refs.loginForm.validate(valid => {
        if (valid) {
          this.loading = true
          this.$store.dispatch('user/login', this.user)
            .then(() => {
              this.$router.push({ path: this.redirect || '/', query: this.otherQuery })
              this.loading = false
            })
            .catch(() => {
              this.loading = false
            })
        } else {
          console.log('error submit!!')
          return false
        }
      })
    },    
    getOtherQuery(query) {
      return Object.keys(query).reduce((acc, cur) => {
        if (cur !== 'redirect') {
          acc[cur] = query[cur]
        }
        return acc
      }, {})
    }    
  }
}
</script>

